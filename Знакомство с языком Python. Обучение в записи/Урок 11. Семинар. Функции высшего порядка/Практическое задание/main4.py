#                                                   Подсказка № 1

# Перед началом работы убедитесь, что строка преобразована в нижний регистр. Это нужно для того, чтобы подсчет символов был регистронезависимым. Преобразование строки к нижнему
# регистру помогает избежать различий между символами разного регистра, так как a и A будут считаться одним и тем же символом.

#                                                   Подсказка № 2

# Используйте filter для выбора уникальных символов. Примените к каждому символу лямбда-функцию, которая проверяет, встречается ли символ в строке ровно один раз. Функция filter
# отфильтрует символы, у которых количество вхождений в строке ровно 1, благодаря проверке внутри лямбда-функции.

#                                                   Подсказка № 3

# Для проверки количества вхождений символа используйте метод count. Он возвращает количество вхождений подстроки в строке. Метод count позволяет узнать, сколько раз символ
# встречается в строке, что необходимо для проверки уникальности символов.

#                                                   Подсказка № 4

# Преобразуйте результат фильтрации в список с помощью list(). Это сделает обработку отфильтрованных символов удобнее. Преобразование результата filter в список позволяет
# работать с ним как с обычным списком и получить доступ к его длине.

#                                                   Подсказка № 5

# Возвращайте длину списка уникальных символов с помощью len(). Это даст количество уникальных символов в строке.

#                                                   Эталонное решение:

def count_unique_characters(string):
    # Приводим строку к нижнему регистру, чтобы сделать подсчет регистронезависимым
    lower_string = string.lower()
    # Используем filter для выбора символов, которые встречаются в строке ровно один раз
    unique_chars = list(filter(lambda c:
    lower_string.count(c.lower()) == 1, lower_string))
    # Выводим уникальные символы (по желанию, можно удалить эту строку)
    print(unique_chars)
    # Возвращаем количество уникальных символов
    return len(unique_chars)
    # Пример использования:
message = "Today is a beautiful day! The sun is shining and the birds are singing."
unique_count = count_unique_characters(message)
print("Количество уникальных символов в строке:", unique_count)
